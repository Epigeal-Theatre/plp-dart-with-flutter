void main() {
  //strings in dart can be enclosed in either single or double quotes
  String name = "Gallagher";
  String occupation = "Business man!";

  print("My name is $name and I am a $occupation!");

  //remember that using var directly will still give the same output

  var name2 = "Daniel";
  var marital_status = "single";

  print("My name is $name2 and I am $marital_status!");
}
