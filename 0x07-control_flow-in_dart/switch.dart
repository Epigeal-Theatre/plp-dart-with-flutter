  int n = 3;
  switch (n) {
    case 1:
      print("The number you entered is 1");

      break;
    case 2:
      print("The number you entered is 2");

      break;
    case 3:
      print("The number you entered is 3");

      break;
    case 4:
      print("The number you entered is 4");

      break;
    case 5:
      print("The number you entered is 5");

      break;
    default:
      print("Enter a valid number below 5");
      break;
  }
}
